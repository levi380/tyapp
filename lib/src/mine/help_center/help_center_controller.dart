  // 将 assets 图片转换为 base64 编码
  Future<String> _convertAssetImageToBase64(String assetPath) async {
    try {
      final ByteData data = await rootBundle.load(assetPath);
      final Uint8List bytes = data.buffer.asUint8List();
      final String base64 = base64Encode(bytes);

      // 根据文件扩展名确定 MIME 类型
      String mimeType = 'image/png';
      if (assetPath.toLowerCase().endsWith('.jpg') ||
          assetPath.toLowerCase().endsWith('.jpeg')) {
        mimeType = 'image/jpeg';
      } else if (assetPath.toLowerCase().endsWith('.webp')) {
        mimeType = 'image/webp';
      } else if (assetPath.toLowerCase().endsWith('.png')) {
        mimeType = 'image/png';
      }

      return 'data:$mimeType;base64,$base64';
    } catch (e) {
      print('加载图片失败: $assetPath, 错误: $e');
      return ''; // 返回空字符串，图片将不显示
    }
  }

  // 处理 HTML 内容中的本地图片路径（assets:// 前缀）
  Future<String> processLocalImages(String htmlContent) async {
    // 匹配 <img src="assets://..."> 或 <img src='assets://...'> 格式
    final RegExp assetImageRegex = RegExp(
      r'<img\s+[^>]*src=(["' "'" ']?)assets://([^"' + "'" + r']+)\1[^>]*>',
      caseSensitive: false,
    );

    String processedContent = htmlContent;
    final matches = assetImageRegex.allMatches(htmlContent);

    for (final match in matches) {
      final String quote = match.group(1)!; // 引号类型（" 或 '）
      final String assetPath = match.group(2)!;
      final String base64Image = await _convertAssetImageToBase64(assetPath);

      if (base64Image.isNotEmpty) {
        // 替换整个 img 标签的 src 属性
        final String originalTag = match.group(0)!;
        final String srcPattern = 'src=${quote}assets://$assetPath$quote';
        final String replacement = 'src=${quote}$base64Image$quote';
        final String newTag = originalTag.replaceFirst(srcPattern, replacement);
        processedContent = processedContent.replaceFirst(originalTag, newTag);
      }
    }

    return processedContent;
  }

